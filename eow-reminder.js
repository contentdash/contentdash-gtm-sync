import 'dotenv/config';
import nodemailer from 'nodemailer';
import { writeFileSync, mkdirSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { getStripeSnapshot } from './stripe-report.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const dateLabel = new Date().toLocaleDateString('en-SG', { weekday: 'long', month: 'short', day: 'numeric' });
const testing = process.env.TESTING_MODE !== 'false';

// Pull live data to give Charlene actual numbers to report on
let stripe = null;
let ar = null;
let stripeContext = '';
let arContext = '';

try {
  stripe = await getStripeSnapshot();
  const failNote = stripe.failedPayments > 0 ? ` <strong style="color:#dc2626">(⚠ ${stripe.failedPayments} failed!)</strong>` : ' <span style="color:#16a34a">✓ all current</span>';
  stripeContext = `
    <tr><td style="padding:6px 8px;border-bottom:1px solid #f4f4f4;color:#888">Platform MRR</td><td style="padding:6px 8px;border-bottom:1px solid #f4f4f4"><strong>$${stripe.totalMRR}</strong> USD equiv.</td></tr>
    <tr><td style="padding:6px 8px;border-bottom:1px solid #f4f4f4;color:#888">Collected (30d)</td><td style="padding:6px 8px;border-bottom:1px solid #f4f4f4"><strong>$${stripe.collectedLast30Days}</strong> via Stripe</td></tr>
    <tr><td style="padding:6px 8px;border-bottom:1px solid #f4f4f4;color:#888">Payment status</td><td style="padding:6px 8px;border-bottom:1px solid #f4f4f4">${stripe.failedPayments} failed${failNote}</td></tr>`;
} catch (e) {
  stripeContext = `<tr><td colspan="2" style="padding:6px 8px;color:#d97706">⚠ Stripe unavailable: ${e.message}</td></tr>`;
}

try {
  const { getARSnapshot } = await import('./ar-check.js');
  ar = await getARSnapshot();
  if (ar.overdue.length === 0) {
    arContext = `<tr><td colspan="2" style="padding:6px 8px;color:#16a34a">✓ No overdue invoices in Xero</td></tr>`;
  } else {
    arContext = ar.overdue.map(i => `
      <tr style="${i.daysOverdue > 14 ? 'background:#fff5f5' : ''}">
        <td style="padding:6px 8px;border-bottom:1px solid #f4f4f4"><strong>${i.contact}</strong></td>
        <td style="padding:6px 8px;border-bottom:1px solid #f4f4f4">${i.currency} ${i.amountDue} · due ${i.dueDate} · <strong style="color:${i.daysOverdue > 30 ? '#dc2626' : '#d97706'}">${i.daysOverdue}d overdue</strong></td>
      </tr>`).join('');
  }
} catch (e) {
  arContext = `<tr><td colspan="2" style="padding:6px 8px;color:#d97706">⚠ Xero AR unavailable</td></tr>`;
}

const html = `<!DOCTYPE html>
<html><head><meta charset="UTF-8">
<style>
  body { font-family: 'Helvetica Neue', Arial, sans-serif; font-size: 13px; color: #111; max-width: 600px; margin: 0 auto; padding: 24px; }
  h2 { font-size: 18px; font-weight: 900; margin-bottom: 4px; }
  .section { margin-bottom: 20px; }
  .section-title { font-size: 11px; font-weight: 800; text-transform: uppercase; letter-spacing: 1px; color: #555; border-bottom: 1px solid #eee; padding-bottom: 5px; margin-bottom: 10px; }
  table { width: 100%; border-collapse: collapse; font-size: 12px; }
  ul { padding-left: 18px; line-height: 1.9; margin: 8px 0; }
  .footer { border-top: 1px solid #eee; padding-top: 10px; font-size: 10px; color: #aaa; margin-top: 24px; }
</style></head><body>
<h2>📋 EOW Report Due Today — ${dateLabel}</h2>
<p style="color:#666">Hi Charlene — please prep and send your EOW report before 5pm today.</p>

<div class="section">
  <div class="section-title">📊 Live Numbers for Your Report</div>
  <table><tbody>${stripeContext}${arContext}</tbody></table>
</div>

<div class="section">
  <div class="section-title">✍ Your EOW Report Should Cover</div>
  <ul>
    <li>Hours logged this week (Clockify summary)</li>
    <li>GTM pipeline — new leads, proposals sent, follow-ups done</li>
    <li>Invoice chases — which overdue invoices did you follow up on?</li>
    <li>Key wins / blockers for Ms. Fleire to know</li>
    <li>Tasks planned for next week</li>
  </ul>
</div>

<p>Send to: <strong>info@contentdash.app</strong> by 5pm today.</p>
<div class="footer">Auto-generated by dasho-ops · Fires every Friday at 4pm · Source: Stripe + Xero</div>
</body></html>`;

// Save to disk
const outDir = path.join(__dirname, 'output');
mkdirSync(outDir, { recursive: true });
writeFileSync(path.join(outDir, `eow-reminder-${new Date().toISOString().slice(0, 10)}.html`), html);
console.log('✓ EOW reminder report saved to output/');

// Email
const appPassword = process.env.GMAIL_APP_PASSWORD;
if (appPassword) {
  const transporter = nodemailer.createTransport({
    service: 'gmail',
    auth: { user: process.env.GMAIL_USER, pass: appPassword },
  });
  const to = testing ? process.env.EMAIL_FLEIRE : process.env.EMAIL_CHARLENE;
  const cc = testing ? undefined : process.env.EMAIL_FLEIRE;
  await transporter.sendMail({
    from: `"DashoContent Ops" <${process.env.GMAIL_USER}>`,
    to,
    cc,
    subject: `${testing ? '[TEST] ' : ''}EOW Report Due Today — ${dateLabel}`,
    html,
  });
  console.log(`✓ EOW email sent to ${to}${testing ? ' (TESTING MODE)' : ''}`);
} else {
  console.log('⚠ GMAIL_APP_PASSWORD not set');
}

// Slack
const webhookUrl = process.env.SLACK_WEBHOOK_URL;
if (webhookUrl) {
  const arSlack = ar
    ? (ar.overdue.length === 0 ? '✅ No overdue invoices' : ar.overdue.map(i => `• *${i.contact}* — ${i.currency} ${i.amountDue} · ${i.daysOverdue}d overdue`).join('\n'))
    : '_Xero unavailable_';
  const mrrSlack = stripe ? `MRR: *$${stripe.totalMRR}* · Collected: *$${stripe.collectedLast30Days}*${stripe.failedPayments > 0 ? ` · ⚠ ${stripe.failedPayments} failed` : ''}` : '_Stripe unavailable_';

  await fetch(webhookUrl, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      blocks: [
        { type: 'header', text: { type: 'plain_text', text: `📋 EOW Report Due Today — ${dateLabel}${testing ? ' [TEST]' : ''}` } },
        { type: 'section', fields: [
          { type: 'mrkdwn', text: `💳 *This week's numbers*\n${mrrSlack}` },
          { type: 'mrkdwn', text: `📋 *Overdue invoices*\n${arSlack}` },
        ]},
        { type: 'section', text: { type: 'mrkdwn', text: 'Please send your EOW report to *info@contentdash.app* by 5pm: hours (Clockify), pipeline updates, invoice chases, wins/blockers, next week plan.' }},
      ]
    }),
  });
  console.log('✓ EOW Slack sent');
}
