import 'dotenv/config';
import nodemailer from 'nodemailer';

const html = `<!DOCTYPE html>
<html><head><meta charset="UTF-8">
<style>
  body { font-family: 'Helvetica Neue', Arial, sans-serif; font-size: 13px; color: #111; max-width: 600px; margin: 0 auto; padding: 24px; }
  h2 { font-size: 18px; font-weight: 900; margin-bottom: 4px; }
  ul { padding-left: 18px; line-height: 1.9; }
  .footer { border-top: 1px solid #eee; padding-top: 10px; font-size: 10px; color: #aaa; margin-top: 24px; }
</style></head><body>
<h2>📋 EOW Report — Due Today</h2>
<p style="color:#666">Hi Charlene — Friday reminder to prep and send your EOW report before end of day.</p>
<p><strong>Your EOW report should cover:</strong></p>
<ul>
  <li>Hours logged this week (Clockify summary)</li>
  <li>GTM pipeline status — any new leads, proposals sent, follow-ups done</li>
  <li>Invoice status — any overdue invoices followed up this week?</li>
  <li>Key wins / blockers for Ms. F to know</li>
  <li>Tasks for next week</li>
</ul>
<p>Send to: <strong>info@contentdash.app</strong> by 5pm today.</p>
<div class="footer">Auto-generated by dasho-ops · Fires every Friday at 4pm</div>
</body></html>`;

const dateLabel = new Date().toLocaleDateString('en-SG', { weekday: 'long', month: 'short', day: 'numeric' });

// Email
const appPassword = process.env.GMAIL_APP_PASSWORD;
if (appPassword) {
  const transporter = nodemailer.createTransport({
    service: 'gmail',
    auth: { user: process.env.GMAIL_USER, pass: appPassword },
  });
  await transporter.sendMail({
    from: `"DashoContent Ops" <${process.env.GMAIL_USER}>`,
    to: process.env.EMAIL_CHARLENE,
    cc: process.env.EMAIL_FLEIRE,
    subject: `EOW Report Due Today — ${dateLabel}`,
    html,
  });
  console.log('✓ EOW email sent to Charlene');
} else {
  console.log('⚠ GMAIL_APP_PASSWORD not set — skipping email');
}

// Slack
const webhookUrl = process.env.SLACK_WEBHOOK_URL;
if (webhookUrl) {
  await fetch(webhookUrl, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      blocks: [
        { type: 'header', text: { type: 'plain_text', text: `📋 EOW Report Due Today — ${dateLabel}` } },
        { type: 'section', text: { type: 'mrkdwn', text: 'Hi Charlene! Your EOW report is due by *5pm today*. Please cover:\n• Hours logged (Clockify)\n• GTM pipeline updates\n• Invoice follow-ups\n• Key wins / blockers\n• Tasks for next week' }},
        { type: 'section', text: { type: 'mrkdwn', text: 'Send to: *info@contentdash.app*' }},
      ]
    }),
  });
  console.log('✓ EOW Slack notification sent');
}
